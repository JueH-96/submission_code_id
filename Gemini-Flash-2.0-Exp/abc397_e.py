    nk = n * k
    edges = []
    for _ in range(nk - 1):
        u, v = map(int, input().split())
        edges.append((u, v))

    def check(paths):
        used = [False] * nk
        for path in paths:
            if len(path) != k:
                return False
            for node in path:
                if not (1 <= node <= nk):
                    return False
                if used[node-1]:
                    return False
                used[node-1] = True
            for i in range(k - 1):
                u, v = path[i], path[i+1]
                found = False
                for edge in edges:
                    if (edge[0] == u and edge[1] == v) or (edge[0] == v and edge[1] == u):
                        found = True
                        break
                if not found:
                    return False
        return True

    import itertools
    
    nodes = list(range(1, nk + 1))
    
    def find_paths(current_paths, remaining_nodes):
        if not remaining_nodes:
            if len(current_paths) == n and check(current_paths):
                return True
            else:
                return False
        
        if len(current_paths) == n:
            return False

        
        first_node = remaining_nodes[0]
        
        
        def extend_path(path, remaining, length):
            if length == k:
                return [path]
            
            last_node = path[-1]
            possible_next_nodes = []
            for edge in edges:
                if edge[0] == last_node and edge[1] in remaining:
                    possible_next_nodes.append(edge[1])
                elif edge[1] == last_node and edge[0] in remaining:
                    possible_next_nodes.append(edge[0])
            
            all_paths = []
            for next_node in possible_next_nodes:
                new_remaining = remaining[:]
                new_remaining.remove(next_node)
                
                new_path = path + [next_node]
                
                all_paths.extend(extend_path(new_path, new_remaining, length + 1))
            
            return all_paths

        
        initial_path = [first_node]
        remaining_without_first = remaining_nodes[1:]
        
        possible_paths = extend_path(initial_path, remaining_without_first, 1)
        
        for path in possible_paths:
            new_remaining = remaining_nodes[:]
            for node in path:
                new_remaining.remove(node)
            
            if find_paths(current_paths + [path], new_remaining):
                return True
        
        return False

    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
        
